// Conversion Rates Helper
export const calculateConversionRates = (data: any) => {
  return {
    view_to_click: ((data.website_clicks / data.total_views) * 100).toFixed(2),
    click_to_call: ((data.calls_booked / data.website_clicks) * 100).toFixed(2),
    call_to_show: ((data.show_ups / data.calls_booked) * 100).toFixed(2),
    show_to_close: ((data.total_closes / data.show_ups) * 100).toFixed(2),
    overall: ((data.total_closes / data.total_views) * 100).toFixed(4),
    revenue_per_view: (data.total_revenue / data.total_views).toFixed(2),
  }
}

